import {
  createGetInitialProps,
  createStylesServer,
  ServerStyles,
} from "@mantine/next";
import Document, {
  DocumentContext,
  Head,
  Html,
  Main,
  NextScript,
} from "next/document";
import { appCache } from "../utils/cache";

// const getInitialProps = createGetInitialProps();
const stylesServer = createStylesServer(appCache);

